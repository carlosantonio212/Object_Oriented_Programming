package programajava;

public class JavaPoo {

    public static void main(String[] args) {
   	    
   		Double x= 0d;
      	 
       	Float y= 10f;
      	 
       	Boolean z=false;
      	 
       	System.out.println(x.MAX_VALUE);
      	 
       	System.out.println(z.logicalXor(false,true));
      	 
       	System.out.println(Integer.toBinaryString(80));
      	 
       	System.out.println(Integer.toHexString(80));
      	 
       	System.out.println(Integer.toOctalString(80));
      	 
       	System.out.println(y.max(10f, 80f));
      	 
       	System.out.println(y.min (10f, 80f));
      	 

   	 
    }

}